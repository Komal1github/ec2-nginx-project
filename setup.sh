# AWS EC2 Web Server Deployment (Nginx)

This project demonstrates deploying a web server on AWS EC2 using Nginx.

## Summary
- Launched EC2 instance (Amazon Linux 2 AMI, instance type t3.micro( eligible free tier)
- Configured SSH (port 22) and HTTP (port 80) access
- Installed and started Nginx
- Deployed and tested web server via public IP

## Key Skills
- AWS EC2
- Linux basics
- Nginx web server
- Cloud networking (Security Groups)

## Result
Web server successfully accessible via browser using EC2 public IP.
